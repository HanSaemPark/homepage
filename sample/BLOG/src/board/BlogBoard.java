package board;

public class BlogBoard {
	private int bdID;
	private String bdTitle;
	private String userEmail;
	private String bdDate;
	private String bdContent;
	private int bdUsed;
	private String bdIP;
	
	
	public String getBdIP() {
		return bdIP;
	}
	public void setBdIP(String bdIP) {
		this.bdIP = bdIP;
	}
	public int getBdID() {
		return bdID;
	}
	public void setBdID(int bdID) {
		this.bdID = bdID;
	}
	public String getBdTitle() {
		return bdTitle;
	}
	public void setBdTitle(String bdTitle) {
		this.bdTitle = bdTitle;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getBdDate() {
		return bdDate;
	}
	public void setBdDate(String bdDate) {
		this.bdDate = bdDate;
	}
	public String getBdContent() {
		return bdContent;
	}
	public void setBdContent(String bdContent) {
		this.bdContent = bdContent;
	}
	public int getBdUsed() {
		return bdUsed;
	}
	public void setBdUsed(int bdUsed) {
		this.bdUsed = bdUsed;
	}
	
	
	
}
